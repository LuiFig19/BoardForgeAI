#!/usr/bin/env node
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { existsSync } from 'node:fs'
import path from 'node:path'
import crypto from 'node:crypto'

const allowedJobTypes = new Set([
  'create_outline_board',
  'create_kicad_project',
  'apply_edge_cuts',
  'scan_kicad_project',
  'find_missing_footprints',
  'link_3d_models',
  'run_kicad_drc',
  'run_kicad_erc',
  'export_gerbers',
  'export_drill_files',
  'export_bom',
  'export_cpl',
  'package_jlcpcb',
  'summarize_project',
])

function argValue(name) {
  const index = process.argv.indexOf(name)
  return index === -1 ? null : process.argv[index + 1] || null
}

function sanitizeName(name) {
  const safe = String(name || 'boardforge-project')
    .trim()
    .replace(/[^a-zA-Z0-9-_ ]/g, '')
    .replace(/\s+/g, '-')
    .slice(0, 64)
    .toLowerCase()
  return safe || 'boardforge-project'
}

function resolveInsideWorkspace(workspace, target) {
  const root = path.resolve(workspace)
  const resolved = path.resolve(root, target)
  if (resolved !== root && !resolved.startsWith(root + path.sep)) {
    throw new Error(`Refusing path outside workspace: ${target}`)
  }
  return resolved
}

function validateJob(job) {
  if (!job || typeof job !== 'object') throw new Error('Job must be a JSON object')
  if (!allowedJobTypes.has(job.type)) throw new Error(`Unsupported job type: ${job.type}`)
  if (job.type !== 'create_outline_board') return
  const board = job.input?.board
  if (!board || typeof board !== 'object') throw new Error('create_outline_board requires input.board')
  if (board.units !== 'mm') throw new Error('MVP only supports millimeter outlines')
  if (!Array.isArray(board.segments) || board.segments.length < 3) throw new Error('Board outline requires at least 3 Edge.Cuts segments')
  for (const segment of board.segments) {
    if (segment.type !== 'line') throw new Error('MVP Edge.Cuts writer currently supports line segments only')
    for (const key of ['start', 'end']) {
      if (!Number.isFinite(segment[key]?.x) || !Number.isFinite(segment[key]?.y)) throw new Error(`Invalid segment ${key} point`)
    }
  }
  if (board.mountingHoles && !Array.isArray(board.mountingHoles)) throw new Error('mountingHoles must be an array')
}

function grLine(segment) {
  return `  (gr_line (start ${segment.start.x} ${segment.start.y}) (end ${segment.end.x} ${segment.end.y})\n    (stroke (width 0.1) (type solid)) (layer "Edge.Cuts") (uuid "${crypto.randomUUID()}"))`
}

function grCircle(hole) {
  const radius = Number((hole.diameterMm / 2).toFixed(3))
  return `  (gr_circle (center ${hole.x} ${hole.y}) (end ${Number((hole.x + radius).toFixed(3))} ${hole.y})\n    (stroke (width 0.1) (type solid)) (fill none) (layer "Edge.Cuts") (uuid "${crypto.randomUUID()}"))`
}

function projectFile(board) {
  return JSON.stringify({
    meta: { version: 1 },
    board: { design_settings: { defaults: {}, rules: {} } },
    boards: [],
    cvpcb: {},
    libraries: {},
    net_settings: {},
    pcbnew: {},
    schematic: {},
    sheets: [],
    text_variables: {
      BOARD_NAME: board.name,
      BOARDFORGE_MODE: 'outline_only',
      BOARDFORGE_EXECUTOR: 'boardforge-plugin-cli',
    },
  }, null, 2)
}

function pcbFile(board) {
  const edgeCuts = board.segments.map(grLine)
  const holes = (board.mountingHoles || []).map(grCircle)
  return `(kicad_pcb (version 20240108) (generator "BoardForge Plugin CLI")\n  (general (thickness 1.6))\n  (paper "A4")\n  (layers\n    (0 "F.Cu" signal)\n    (31 "B.Cu" signal)\n    (36 "B.SilkS" user)\n    (37 "F.SilkS" user)\n    (38 "B.Mask" user)\n    (39 "F.Mask" user)\n    (44 "Edge.Cuts" user)\n    (45 "Margin" user)\n    (48 "B.Fab" user)\n    (49 "F.Fab" user)\n  )\n  (setup (pad_to_mask_clearance 0))\n${edgeCuts.join('\n')}\n${holes.join('\n')}\n  (gr_text "BoardForge outline-only local job" (at 2 -3 0) (layer "Cmts.User")\n    (effects (font (size 1.2 1.2) (thickness 0.15))) (uuid "${crypto.randomUUID()}"))\n)\n`
}

function readmeFile(job, board) {
  return `# ${board.name}\n\nGenerated by BoardForge Plugin CLI from structured job ${job.id}.\n\n- Job type: ${job.type}\n- Mode: outline-only Edge.Cuts board\n- Units: ${board.units}\n- Mounting holes: ${(board.mountingHoles || []).length}\n- Human review required before manufacturing\n\nNo schematic, footprints, routed copper, BOM, CPL, Gerbers, or drill files are generated by this MVP command.\n`
}

async function createOutlineBoard(job, workspace) {
  const board = job.input.board
  const safeName = sanitizeName(job.input.projectName || board.name)
  const projectDir = resolveInsideWorkspace(workspace, safeName)
  if (existsSync(projectDir) && !job.allowOverwrite) {
    throw new Error(`Project already exists. Set allowOverwrite true to replace files: ${projectDir}`)
  }
  const files = [
    { path: `${safeName}.kicad_pro`, content: projectFile(board) },
    { path: `${safeName}.kicad_pcb`, content: pcbFile(board) },
    { path: 'README.md', content: readmeFile(job, board) },
  ]
  if (!job.dryRun) {
    await mkdir(projectDir, { recursive: true })
    for (const file of files) {
      await writeFile(resolveInsideWorkspace(projectDir, file.path), file.content, 'utf8')
    }
  }
  return {
    id: `${job.id || 'job'}_result`,
    jobId: job.id,
    status: 'complete',
    projectPath: projectDir,
    generatedFiles: files.map((file) => path.join(projectDir, file.path)),
    warnings: [],
    errors: [],
  }
}

async function main() {
  const jobPath = argValue('--job')
  const workspaceArg = argValue('--workspace') || process.cwd()
  if (!jobPath) throw new Error('Missing --job path/to/job.json')
  const workspace = path.resolve(workspaceArg)
  const job = JSON.parse(await readFile(path.resolve(jobPath), 'utf8'))
  validateJob(job)
  if (job.type !== 'create_outline_board') {
    const result = { id: `${job.id || 'job'}_result`, jobId: job.id, status: 'not_implemented', warnings: [`${job.type} adapter is scaffolded but not implemented in CLI MVP`], errors: [] }
    console.log(JSON.stringify(result, null, 2))
    return
  }
  const result = await createOutlineBoard(job, workspace)
  console.log(JSON.stringify(result, null, 2))
}

main().catch((error) => {
  console.error(JSON.stringify({ status: 'failed', errors: [error.message] }, null, 2))
  process.exit(1)
})
